numero = int(input('Escolha um numero entre 1 e 7 : '))

if numero == 1:
    print('1 É IGUAL A DOMINGO')
elif numero == 2:
    print('2 É IGUAL A SEGUNDA-FEIRA')
elif numero == 3:
    print('3 É IGUAL A TERÇA-FEIRA')
elif numero == 4:
    print('4 É IGUAL A QUARTA-FEIRA')
elif numero == 5:
    print('5 É IGUAL A QUINTA-FEIRA')
elif numero == 6:
    print('6 É IGUAL A SEXTA_FEIRA')
elif numero == 7:
    print('7 É IGUAL A SABADO')
else:
    print('NUMERO INVALIDO')
    

